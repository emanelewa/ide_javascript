function getLetter(s) {
    switch (s[0]) {
        case 'a':
        case 'i':
        case 'o':
        case 'u':
            document.write("A");
            break;
        case 'b':
        case 'c':
        case 'd':
        case 'f':
        case 'g':
            document.write("B");
            break;
        case 'h':
        case 'j':
        case 'k':
        case 'l':
        case 'm':
            document.write("C");
            break;

    }
}getLetter("adfgt");